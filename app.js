function setupCategoryStage() {
  const section = document.getElementById("category-stage");
  const stage = document.getElementById("category-stage-track");
  const prevBtn = document.getElementById("category-stage-prev");
  const nextBtn = document.getElementById("category-stage-next");
  const openBtn = document.getElementById("category-stage-open");
  const titleEl = document.getElementById("category-stage-title");
  const copyEl = document.getElementById("category-stage-copy");
  const ghostEl = document.getElementById("category-stage-ghost-text");
  if (!section || !stage) return;

  let activeIndex = 0;
  let isAnimating = false;

  // Preload all stage images
  CATEGORY_STAGE_ITEMS.forEach((item) => {
    const img = new Image();
    img.src = item.src;
  });

  /* Carousel: all items in DOM for spin animation.
     At rest only center is visible — left/right park off-screen (CSS). */
  stage.innerHTML = CATEGORY_STAGE_ITEMS.map((item, i) => `
    <div class="category-stage-item" data-stage-index="${i}" data-category="${item.category}" role="button" tabindex="-1" aria-label="">
      <img src="${item.src}" alt="" draggable="false" width="480" height="640" decoding="async">
    </div>
  `).join("");

  const items = Array.from(stage.querySelectorAll(".category-stage-item"));

  const categoryLabel = (category) => {
    if (window.i18n && typeof window.i18n.t === "function") {
      return window.i18n.t(`catalog_cat_${category}`);
    }
    return category;
  };

  const categoryDesc = (category) => {
    if (window.i18n && typeof window.i18n.t === "function") {
      return window.i18n.t(`category_stage_desc_${category}`);
    }
    return "";
  };

  /**
   * One font size for ALL ghost titles, sized so the longest label
   * («Пирожные с ягодами» / KK equivalent) fits the screen — desktop & mobile.
   */
  const syncGhostFontSize = () => {
    if (!ghostEl || !section) return;
    const parent = ghostEl.parentElement;
    if (!parent) return;

    let longest = "ПИРОЖНЫЕ С ЯГОДАМИ";
    CATEGORY_STAGE_ITEMS.forEach((item) => {
      const t = (categoryLabel(item.category) || item.ghost || "").toUpperCase();
      if (t.length > longest.length) longest = t;
    });

    const maxW = Math.max(80, parent.clientWidth - 8);
    const prevText = ghostEl.textContent;
    const prevSize = ghostEl.style.fontSize;

    ghostEl.style.transform = "translate(-50%, -50%)";
    ghostEl.style.fontSize = "200px";
    ghostEl.textContent = longest;

    let lo = 14;
    let hi = Math.min(200, Math.floor(parent.clientWidth * 0.28));
    let best = lo;
    while (lo <= hi) {
      const mid = (lo + hi) >> 1;
      ghostEl.style.fontSize = `${mid}px`;
      if (ghostEl.scrollWidth <= maxW) {
        best = mid;
        lo = mid + 1;
      } else {
        hi = mid - 1;
      }
    }

    const sizePx = `${best}px`;
    section.style.setProperty("--ghost-font-size", sizePx);
    ghostEl.style.fontSize = sizePx;
    ghostEl.style.transform = "translate(-50%, -50%)";
    ghostEl.textContent = prevText || longest;
    if (prevSize && !prevText) ghostEl.style.fontSize = sizePx;
  };

  const openCategoryInCatalog = (category) => {
    if (!category) return;
    const targetTab = Array.from(document.querySelectorAll(".tab-btn")).find(
      (btn) => btn.getAttribute("data-category") === category
    );
    if (targetTab) {
      if (typeof triggerHapticFeedback === "function") triggerHapticFeedback();
      targetTab.click();
    } else if (typeof renderCatalog === "function") {
      renderCatalog(category);
    }
    const catalog = document.getElementById("catalog");
    if (catalog) catalog.scrollIntoView({ behavior: "smooth" });
  };

  const applyRoles = () => {
    const n = CATEGORY_STAGE_ITEMS.length;
    const roles = {
      center: activeIndex,
      left: (activeIndex + n - 1) % n,
      right: (activeIndex + 1) % n,
      back: (activeIndex + 2) % n
    };
    const current = CATEGORY_STAGE_ITEMS[activeIndex];

    items.forEach((el, i) => {
      el.classList.remove("is-center", "is-left", "is-right", "is-back");
      if (i === roles.center) el.classList.add("is-center");
      else if (i === roles.left) el.classList.add("is-left");
      else if (i === roles.right) el.classList.add("is-right");
      else el.classList.add("is-back");
      el.setAttribute("tabindex", i === roles.center ? "0" : "-1");
      el.setAttribute("aria-label", categoryLabel(CATEGORY_STAGE_ITEMS[i].category));
    });

    section.style.setProperty("--stage-accent", current.bg);
    section.style.setProperty("--stage-accent-2", current.bg2 || current.bg);
    section.style.setProperty("--stage-accent-3", current.bg3 || current.bg);

    if (titleEl) {
      titleEl.textContent = categoryLabel(current.category);
      titleEl.removeAttribute("data-i18n");
    }
    if (copyEl) {
      copyEl.textContent = categoryDesc(current.category);
      copyEl.removeAttribute("data-i18n");
    }
    if (ghostEl) {
      // Full category title for every slide — same font size as the longest one
      ghostEl.textContent = (categoryLabel(current.category) || current.ghost || "").toUpperCase();
      ghostEl.style.transform = "translate(-50%, -50%)";
    }
  };

  let ghostResizeTimer = null;
  window.addEventListener("resize", () => {
    clearTimeout(ghostResizeTimer);
    ghostResizeTimer = setTimeout(() => {
      syncGhostFontSize();
      applyRoles();
    }, 80);
  });

  const navigate = (dir) => {
    if (isAnimating) return;
    isAnimating = true;
    const n = CATEGORY_STAGE_ITEMS.length;
    activeIndex = dir === "next" ? (activeIndex + 1) % n : (activeIndex + n - 1) % n;
    applyRoles();
    if (typeof triggerHapticFeedback === "function") triggerHapticFeedback();
    setTimeout(() => {
      isAnimating = false;
    }, 650);
  };

  if (prevBtn) prevBtn.addEventListener("click", () => navigate("prev"));
  if (nextBtn) nextBtn.addEventListener("click", () => navigate("next"));

  if (openBtn) {
    openBtn.addEventListener("click", () => {
      openCategoryInCatalog(CATEGORY_STAGE_ITEMS[activeIndex].category);
    });
  }

  // Swipes (Pointer Events cover touch + mouse; no double-fire with touch*)
  // finger/drag left → next, right → prev; vertical scroll still works (touch-action: pan-y)
  let swipeStartX = 0;
  let swipeStartY = 0;
  let swipeTracking = false;
  let swipePointerId = null;
  let swipeIgnoreClick = false;
  const SWIPE_MIN_PX = 46;
  const SWIPE_RATIO = 1.15; // horizontal must beat vertical

  section.addEventListener("pointerdown", (e) => {
    if (e.pointerType === "mouse" && e.button !== 0) return;
    if (e.target.closest("button, a, input, textarea, select")) return;
    swipeTracking = true;
    swipePointerId = e.pointerId;
    swipeStartX = e.clientX;
    swipeStartY = e.clientY;
  });

  const endSwipe = (e) => {
    if (!swipeTracking) return;
    if (swipePointerId !== null && e.pointerId !== swipePointerId) return;
    swipeTracking = false;
    swipePointerId = null;
    const dx = e.clientX - swipeStartX;
    const dy = e.clientY - swipeStartY;
    if (Math.abs(dx) < SWIPE_MIN_PX) return;
    if (Math.abs(dx) < Math.abs(dy) * SWIPE_RATIO) return;
    swipeIgnoreClick = true;
    setTimeout(() => {
      swipeIgnoreClick = false;
    }, 450);
    navigate(dx < 0 ? "next" : "prev");
  };

  section.addEventListener("pointerup", endSwipe);
  section.addEventListener("pointercancel", () => {
    swipeTracking = false;
    swipePointerId = null;
  });

  items.forEach((el, i) => {
    el.addEventListener("click", () => {
      if (swipeIgnoreClick) return;
      if (!el.classList.contains("is-center")) return;
      openCategoryInCatalog(CATEGORY_STAGE_ITEMS[i].category);
    });
    el.addEventListener("keydown", (e) => {
      if (!el.classList.contains("is-center")) return;
      if (e.key === "Enter" || e.key === " ") {
        e.preventDefault();
        openCategoryInCatalog(CATEGORY_STAGE_ITEMS[i].category);
      }
    });
  });

  document.addEventListener("keydown", (e) => {
    if (e.key !== "ArrowLeft" && e.key !== "ArrowRight") return;
    const rect = section.getBoundingClientRect();
    if (rect.bottom < 80 || rect.top > window.innerHeight * 0.65) return;
    navigate(e.key === "ArrowRight" ? "next" : "prev");
  });

  if (window.i18n && typeof window.i18n.onLanguageChange === "function") {
    window.i18n.onLanguageChange(() => {
      syncGhostFontSize();
      applyRoles();
    });
  }

  // Size once to longest label, then show active category
  requestAnimationFrame(() => {
    syncGhostFontSize();
    applyRoles();
  });
}

function setupBestsellersCarousel() {
  const grid = document.getElementById("bestsellers-grid");
  const wrapper = document.querySelector(".bestsellers-carousel-wrapper");
  if (!grid || !wrapper) return;

  const prevBtn = wrapper.querySelector(".prev-btn");
  const nextBtn = wrapper.querySelector(".next-btn");
  const rail = document.getElementById("bestsellers-scroll-rail");
  const thumb = document.getElementById("bestsellers-scroll-thumb");

  // Brief highlight while scrolling (for side chevrons visibility)
  let scrollHideTimer = null;
  const markScrolling = () => {
    grid.classList.add("is-scrolling");
    wrapper.classList.add("is-scrolling");
    if (scrollHideTimer) clearTimeout(scrollHideTimer);
    scrollHideTimer = setTimeout(() => {
      grid.classList.remove("is-scrolling");
      wrapper.classList.remove("is-scrolling");
    }, 900);
  };

  // --- Custom rounded rail (no OS scrollbar arrows) ---
  let railDragging = false;
  let railDragOffsetX = 0;

  const updateCustomRail = () => {
    if (!rail || !thumb) return;
    const maxScroll = grid.scrollWidth - grid.clientWidth;
    if (maxScroll <= 2) {
      rail.classList.add("is-disabled");
      thumb.style.width = "100%";
      thumb.style.transform = "translateX(0)";
      return;
    }
    rail.classList.remove("is-disabled");
    const railW = rail.clientWidth;
    const thumbW = Math.max(36, (grid.clientWidth / grid.scrollWidth) * railW);
    const maxThumbLeft = Math.max(0, railW - thumbW);
    const left = maxScroll > 0 ? (grid.scrollLeft / maxScroll) * maxThumbLeft : 0;
    thumb.style.width = `${thumbW}px`;
    thumb.style.transform = `translateX(${left}px)`;
  };

  const scrollFromRailClientX = (clientX) => {
    if (!rail) return;
    const maxScroll = grid.scrollWidth - grid.clientWidth;
    if (maxScroll <= 0) return;
    const rect = rail.getBoundingClientRect();
    const railW = rect.width;
    const thumbW = thumb ? thumb.offsetWidth : 40;
    const maxThumbLeft = Math.max(0, railW - thumbW);
    let left = clientX - rect.left - railDragOffsetX;
    left = Math.max(0, Math.min(maxThumbLeft, left));
    grid.scrollLeft = maxThumbLeft > 0 ? (left / maxThumbLeft) * maxScroll : 0;
  };

  if (rail && thumb) {
    rail.addEventListener("pointerdown", (e) => {
      // Click on track jumps; drag on thumb moves
      const thumbRect = thumb.getBoundingClientRect();
      const onThumb = e.clientX >= thumbRect.left && e.clientX <= thumbRect.right;
      railDragging = true;
      railDragOffsetX = onThumb ? e.clientX - thumbRect.left : thumb.offsetWidth / 2;
      rail.setPointerCapture?.(e.pointerId);
      scrollFromRailClientX(e.clientX);
      markScrolling();
      e.preventDefault();
    });
    rail.addEventListener("pointermove", (e) => {
      if (!railDragging) return;
      scrollFromRailClientX(e.clientX);
      markScrolling();
    });
    const endRailDrag = (e) => {
      if (!railDragging) return;
      railDragging = false;
      try {
        rail.releasePointerCapture?.(e.pointerId);
      } catch (_) { /* ignore */ }
    };
    rail.addEventListener("pointerup", endRailDrag);
    rail.addEventListener("pointercancel", endRailDrag);
  }

  grid.addEventListener("scroll", () => {
    markScrolling();
    updateCustomRail();
  }, { passive: true });
  grid.addEventListener("wheel", markScrolling, { passive: true });
  grid.addEventListener("touchmove", markScrolling, { passive: true });

  window.addEventListener("resize", updateCustomRail, { passive: true });
  // After products render, layout may change
  if (typeof ResizeObserver !== "undefined") {
    const ro = new ResizeObserver(() => updateCustomRail());
    ro.observe(grid);
  }
  // Initial + delayed (images/cards may load later)
  updateCustomRail();
  setTimeout(updateCustomRail, 100);
  setTimeout(updateCustomRail, 500);
  setTimeout(updateCustomRail, 1500);

  const scrollByCard = (dir) => {
    const card = grid.querySelector(".product-card");
    const gap = parseFloat(getComputedStyle(grid).gap) || 36;
    const step = card ? card.getBoundingClientRect().width + gap : 440;
    grid.scrollBy({ left: dir * step, behavior: "smooth" });
  };

  if (prevBtn) {
    prevBtn.addEventListener("click", () => {
      triggerHapticFeedback();
      markScrolling();
      scrollByCard(-1);
    });
  }
  if (nextBtn) {
    nextBtn.addEventListener("click", () => {
      triggerHapticFeedback();
      markScrolling();
      scrollByCard(1);
    });
  }

  // Mouse Drag-to-Scroll (LKM drag)
  let isDown = false;
  let hasMoved = false;
  let startX;
  let scrollLeft;

  grid.addEventListener("mousedown", (e) => {
    // Only drag with left click
    if (e.button !== 0) return;
    isDown = true;
    hasMoved = false;
    grid.classList.add("active-drag");
    startX = e.pageX - grid.offsetLeft;
    scrollLeft = grid.scrollLeft;
  });

  grid.addEventListener("mouseleave", () => {
    isDown = false;
    grid.classList.remove("active-drag");
  });

  grid.addEventListener("mouseup", () => {
    isDown = false;
    grid.classList.remove("active-drag");
  });

  grid.addEventListener("mousemove", (e) => {
    if (!isDown) return;
    const x = e.pageX - grid.offsetLeft;
    const walk = (x - startX) * 1.5; // Scroll speed modifier
    if (Math.abs(x - startX) > 5) {
      hasMoved = true;
      markScrolling();
    }
    grid.scrollLeft = scrollLeft - walk;
  });

  // Capture click events and block them if we dragged
  grid.addEventListener("click", (e) => {
    if (hasMoved) {
      e.preventDefault();
      e.stopPropagation();
      hasMoved = false;
    }
  }, true);
}

