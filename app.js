/**
 * @param {{
 *   message: string,
 *   title?: string,
 *   confirm?: boolean,
 *   danger?: boolean,
 *   okLabel?: string,
 *   cancelLabel?: string,
 * }} opts
 * @returns {Promise<boolean>} true if primary (OK) pressed
 */
function showSiteDialog(opts = {}) {
  const message = opts.message || "";
  const confirmMode = !!opts.confirm;
  const danger = !!opts.danger;
  const { overlay, titleEl, msgEl, okBtn, cancelBtn, iconWrap } = getSiteDialogEls();

  if (!overlay || !msgEl || !okBtn) {
    // Fallback only if markup missing
    if (confirmMode) return Promise.resolve(window.confirm(message));
    window.alert(message);
    return Promise.resolve(true);
  }

  // Close previous pending dialog
  if (siteDialogResolver) {
    siteDialogResolver(false);
    siteDialogResolver = null;
  }

  const t = (key, fb) => (window.i18n ? window.i18n.t(key) : fb);
  const title =
    opts.title ||
    (confirmMode
      ? t("dialog_title_confirm", "Подтверждение")
      : t("dialog_title_notice", "Внимание"));

  if (titleEl) titleEl.textContent = title;
  msgEl.textContent = message;

  if (cancelBtn) {
    cancelBtn.classList.toggle("hidden", !confirmMode);
    cancelBtn.textContent =
      opts.cancelLabel || t("dialog_btn_cancel", "Отмена");
  }
  okBtn.textContent =
    opts.okLabel ||
    (confirmMode
      ? t("dialog_btn_confirm", "Подтвердить")
      : t("dialog_btn_ok", "Понятно"));

  if (iconWrap) {
    iconWrap.classList.toggle("is-warn", !!danger);
    const info = iconWrap.querySelector(".site-dialog-icon-info");
    const warn = iconWrap.querySelector(".site-dialog-icon-warn");
    if (info) info.classList.toggle("hidden", !!danger);
    if (warn) warn.classList.toggle("hidden", !danger);
  }

  return new Promise((resolve) => {
    siteDialogResolver = resolve;
    if (typeof lockBodyScroll === "function" && !overlay.classList.contains("open")) {
      lockBodyScroll();
      overlay.dataset.scrollLocked = "1";
    }
    overlay.classList.add("open");
    // Focus primary action for a11y
    setTimeout(() => okBtn.focus(), 30);
  });
}

function showAlert(message, title) {
  return showSiteDialog({ message, title, confirm: false });
}

function showConfirm(message, title) {
  return showSiteDialog({ message, title, confirm: true, danger: true });
}

function setupSiteDialog() {
  const { overlay, okBtn, cancelBtn } = getSiteDialogEls();
  if (!overlay) return;

  if (okBtn) {
    okBtn.addEventListener("click", () => {
      triggerHapticFeedback();
      closeSiteDialog(true);
    });
  }
  if (cancelBtn) {
    cancelBtn.addEventListener("click", () => {
      triggerHapticFeedback();
      closeSiteDialog(false);
    });
  }
  overlay.addEventListener("click", (e) => {
    if (e.target === overlay) {
      closeSiteDialog(false);
    }
  });
  document.addEventListener("keydown", (e) => {
    if (e.key === "Escape" && overlay.classList.contains("open")) {
      closeSiteDialog(false);
    }
  });
}

/** Working hours for pickup slots (local time). Last slot 19:30. */
const PICKUP_HOUR_OPEN = 9;
const PICKUP_HOUR_CLOSE = 20;
const PICKUP_SLOT_STEP_MIN = 30;
const PICKUP_PREP_BUFFER_MIN = 45;
const PICKUP_MAX_DAYS_AHEAD = 14;

function toInputDateValue(date) {
  const y = date.getFullYear();
  const m = String(date.getMonth() + 1).padStart(2, "0");
  const d = String(date.getDate()).padStart(2, "0");
  return `${y}-${m}-${d}`;
}

function parseLocalDate(dateStr) {
  const [y, m, d] = String(dateStr || "").split("-").map(Number);
  if (!y || !m || !d) return null;
  return new Date(y, m - 1, d, 0, 0, 0, 0);
}

/** Returns HH:MM slots for a given YYYY-MM-DD, skipping past times for today. */
function generatePickupTimeSlots(dateStr) {
  const day = parseLocalDate(dateStr);
  if (!day) return [];
  const now = new Date();
  const isToday = day.toDateString() === now.toDateString();
  const minTime = new Date(now.getTime() + PICKUP_PREP_BUFFER_MIN * 60 * 1000);
  const slots = [];

  for (let mins = PICKUP_HOUR_OPEN * 60; mins < PICKUP_HOUR_CLOSE * 60; mins += PICKUP_SLOT_STEP_MIN) {
    const h = Math.floor(mins / 60);
    const m = mins % 60;
    const slotDate = new Date(day.getFullYear(), day.getMonth(), day.getDate(), h, m, 0, 0);
    if (isToday && slotDate < minTime) continue;
    slots.push(`${String(h).padStart(2, "0")}:${String(m).padStart(2, "0")}`);
  }
  return slots;
}

function formatPickupDisplay(dateStr, timeStr) {
  if (!dateStr || !timeStr) return "";
  const day = parseLocalDate(dateStr);
  if (!day) return `${dateStr} ${timeStr}`;
  try {
    const dateLabel = day.toLocaleDateString("ru-RU", {
      weekday: "short",
      day: "numeric",
      month: "long"
    });
    return `${dateLabel}, ${timeStr}`;
  } catch (_) {
    return `${dateStr} ${timeStr}`;
  }
}

function refreshPickupTimeSlots(preserveValue = true) {
  const dateInput = document.getElementById("checkout-pickup-date");
  const timeSelect = document.getElementById("checkout-pickup-time");
  if (!dateInput || !timeSelect) return;

  const prev = preserveValue ? timeSelect.value : "";
  const slots = generatePickupTimeSlots(dateInput.value);
  const ph = window.i18n ? window.i18n.t("cart_pickup_time_ph") : "Выберите время";

  timeSelect.innerHTML = `<option value="">${escapeHTML(ph)}</option>` +
    slots.map((s) => `<option value="${s}">${s}</option>`).join("");

  if (prev && slots.includes(prev)) {
    timeSelect.value = prev;
  } else {
    timeSelect.value = "";
  }
}

function setupPickupDateLimits() {
  const dateInput = document.getElementById("checkout-pickup-date");
  if (!dateInput) return;
  const today = new Date();
  const max = new Date();
  max.setDate(max.getDate() + PICKUP_MAX_DAYS_AHEAD);
  dateInput.min = toInputDateValue(today);
  dateInput.max = toInputDateValue(max);
  if (!dateInput.value) {
    dateInput.value = toInputDateValue(today);
  }
  // If today has no slots left, jump to tomorrow
  let slots = generatePickupTimeSlots(dateInput.value);
  if (!slots.length) {
    const tomorrow = new Date();
    tomorrow.setDate(tomorrow.getDate() + 1);
    dateInput.value = toInputDateValue(tomorrow);
  }
  refreshPickupTimeSlots(false);
}

