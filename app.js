/**
 * Mobile heat / jank guards:
 * - pause hero video when off-screen (video decode is the main phone heater)
 * - pause infinite marquees (ticker, reviews) when not visible
 * - mark reduced-motion / save-data / coarse pointer for CSS
 */
function setupRuntimePerformance() {
  const root = document.documentElement;
  if (window.matchMedia("(prefers-reduced-motion: reduce)").matches) {
    root.classList.add("prefers-reduced-motion");
  }
  if (navigator.connection && navigator.connection.saveData) {
    root.classList.add("save-data");
  }
  // Phones: lighter GPU effects via CSS
  if (window.matchMedia("(hover: none) and (pointer: coarse)").matches) {
    root.classList.add("is-touch-device");
  }

  if (!("IntersectionObserver" in window)) return;

  // 1) Hero video — pause off-screen, resume when visible
  const heroVideo = document.querySelector("video.hero-video-bg");
  if (heroVideo) {
    const heroIo = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          if (entry.isIntersecting && entry.intersectionRatio > 0.2) {
            if (heroVideo.paused && heroVideo.dataset.videoLoaded === "1") {
              const p = heroVideo.play();
              if (p && typeof p.catch === "function") p.catch(() => {});
            }
            heroVideo.classList.remove("is-offscreen");
          } else {
            if (!heroVideo.paused) heroVideo.pause();
            heroVideo.classList.add("is-offscreen");
          }
        });
      },
      { threshold: [0, 0.2, 0.5], rootMargin: "40px 0px" }
    );
    heroIo.observe(heroVideo);
  }

  // 2) Infinite CSS marquees — animation-play-state: paused when off-screen
  const marqueeTargets = [
    ...document.querySelectorAll(".ticker-tape-wrapper"),
    ...document.querySelectorAll(".reviews-marquee")
  ];
  if (marqueeTargets.length) {
    const marqueeIo = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          entry.target.classList.toggle("is-marquee-paused", !entry.isIntersecting);
        });
      },
      { rootMargin: "80px 0px", threshold: 0 }
    );
    marqueeTargets.forEach((el) => marqueeIo.observe(el));
  }

  // 3) Category stage grain off-screen (SVG noise is expensive if always painted)
  const stageSection = document.getElementById("category-stage");
  if (stageSection) {
    const stageIo = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          stageSection.classList.toggle("is-stage-idle", !entry.isIntersecting);
        });
      },
      { rootMargin: "60px 0px", threshold: 0 }
    );
    stageIo.observe(stageSection);
  }
}

/** Defer hero video until after first paint / preloader (poster shows immediately). */
function setupLazyHeroVideo() {
  const video = document.querySelector("video.hero-video-bg[data-lazy-video]");
  if (!video || video.dataset.videoLoaded === "1") return;

  const tryPlay = () => {
    video.muted = true;
    video.defaultMuted = true;
    video.playsInline = true;
    const p = video.play();
    if (p && typeof p.catch === "function") p.catch(() => {});
  };

  const activateSources = () => {
    if (video.dataset.videoLoaded === "1") return;
    video.dataset.videoLoaded = "1";
    video.classList.add("is-loading");
    video.muted = true;
    video.defaultMuted = true;
    video.setAttribute("playsinline", "");
    video.setAttribute("webkit-playsinline", "");

    // Wire real sources (only files that exist in /Videos)
    video.querySelectorAll("source[data-src]").forEach((source) => {
      const src = source.getAttribute("data-src");
      if (!src) return;
      source.setAttribute("src", src);
      source.removeAttribute("data-src");
    });

    const markReady = () => {
      video.classList.remove("is-loading");
      video.classList.add("is-ready");
      tryPlay();
    };

    video.addEventListener("canplay", markReady, { once: true });
    video.addEventListener("loadeddata", markReady, { once: true });

    // If selected source fails, drop it and try the next <source>
    video.addEventListener("error", () => {
      const current = video.querySelector("source[src]");
      if (current && current.nextElementSibling && current.nextElementSibling.tagName === "SOURCE") {
        current.remove();
        try {
          video.load();
        } catch (_) {
          /* ignore */
        }
        tryPlay();
      } else {
        video.classList.remove("is-loading");
      }
    });

    try {
      video.load();
    } catch (_) {
      /* ignore */
    }
    tryPlay();
  };

  const schedule = () => {
    if ("requestIdleCallback" in window) {
      requestIdleCallback(() => activateSources(), { timeout: 1200 });
    } else {
      setTimeout(activateSources, 400);
    }
  };

  // Wait for preloader to finish so video doesn't compete with first paint
  if (document.body.classList.contains("preloader-active")) {
    const mo = new MutationObserver(() => {
      if (!document.body.classList.contains("preloader-active")) {
        mo.disconnect();
        schedule();
      }
    });
    mo.observe(document.body, { attributes: true, attributeFilter: ["class"] });
    // Fallback if preloader never clears
    setTimeout(() => {
      mo.disconnect();
      if (video.dataset.videoLoaded !== "1") schedule();
    }, 3500);
  } else {
    requestAnimationFrame(() => schedule());
  }
}

const LEAFLET_CSS =
  "https://cdn.jsdelivr.net/npm/leaflet@1.9.4/dist/leaflet.css";
const LEAFLET_JS =
  "https://cdn.jsdelivr.net/npm/leaflet@1.9.4/dist/leaflet.js";

function loadStylesheetOnce(href) {
  return new Promise((resolve, reject) => {
    const existing = document.querySelector(`link[data-lazy-href="${href}"], link[href="${href}"]`);
    if (existing) {
      if (existing.sheet || existing.dataset.loaded === "1") resolve();
      else existing.addEventListener("load", () => resolve(), { once: true });
      return;
    }
    const link = document.createElement("link");
    link.rel = "stylesheet";
    link.href = href;
    link.dataset.lazyHref = href;
    link.onload = () => {
      link.dataset.loaded = "1";
      resolve();
    };
    link.onerror = () => reject(new Error("Failed to load CSS: " + href));
    document.head.appendChild(link);
  });
}

function loadScriptOnce(src) {
  return new Promise((resolve, reject) => {
    if (typeof L !== "undefined") {
      resolve();
      return;
    }
    const existing = document.querySelector(`script[data-lazy-src="${src}"]`);
    if (existing) {
      existing.addEventListener("load", () => resolve(), { once: true });
      existing.addEventListener("error", () => reject(new Error("Failed to load JS: " + src)), { once: true });
      return;
    }
    const script = document.createElement("script");
    script.src = src;
    script.async = true;
    script.dataset.lazySrc = src;
    script.onload = () => resolve();
    script.onerror = () => reject(new Error("Failed to load JS: " + src));
    document.body.appendChild(script);
  });
}

async function ensureLeafletLoaded() {
  if (typeof L !== "undefined") return;
  await loadStylesheetOnce(LEAFLET_CSS);
  await loadScriptOnce(LEAFLET_JS);
  if (typeof L === "undefined") {
    throw new Error("Leaflet global L not available after load");
  }
}

