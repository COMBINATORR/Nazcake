// Open Product Preview Modal
let activePreviewProductId = null;
function openProductPreview(id) {
  const p = products.find(prod => prod.id === id);
  if (!p) return;

  const { name, price, sizeOptions, ingredients, unit, image, isCustomName } = p;

  const tName = isCustomName ? name : (window.i18n ? window.i18n.t(`p_${id}_name`) : name);
  const tDesc = getProductDesc(p);
  // Prefer i18n; if key missing or i18n failed to load, use product.ingredients from app data
  let tIngredients = ingredients;
  if (window.i18n) {
    const tr = window.i18n.t(`p_${id}_ingredients`);
    if (tr && tr !== `p_${id}_ingredients`) tIngredients = tr;
  }
  const tUnit = window.i18n ? window.i18n.t(getUnitTranslationKey(unit)) : unit;

  activePreviewProductId = id;
  modalProductImg.src = image;
  modalProductImg.alt = tName;
  modalProductTitle.textContent = tName;
  modalProductDesc.textContent = tDesc;
  // Highlight allergen line in composition accordion
  if (modalProductIngredients) {
    const ing = String(tIngredients || "");
    const split = ing.split(/(?=(?:Аллергены|Аллергендер):)/);
    if (split.length > 1) {
      modalProductIngredients.innerHTML =
        `${escapeHTML(split[0].trim())} <span class="modal-allergens">${escapeHTML(split[1].trim())}</span>`;
    } else {
      modalProductIngredients.textContent = ing;
    }
  }
  modalQtyVal.textContent = 1; // Reset qty in modal

  let selectedSize = null;
  let selectedPrice = price;

  // Reset accordions: ingredients closed; size open only when options exist
  const ingredientsGroup = document.getElementById("modal-ingredients-group");
  if (ingredientsGroup) ingredientsGroup.open = false;

  if (sizeOptions && sizeOptions.length > 0) {
    if (modalSizeGroup && modalSizeContainer) {
      modalSizeGroup.classList.remove("hidden");
      modalSizeGroup.open = true;

      modalSizeContainer.innerHTML = sizeOptions.map((opt, index) => {
        const isActive = index === 0 ? "active" : "";
        const sizeLabel = escapeHTML(getSizeOptionLabel(opt.size));
        return `<button type="button" class="size-option-btn ${isActive}" data-index="${index}" data-price="${opt.price}" data-size="${escapeHTML(opt.size)}">${sizeLabel}</button>`;
      }).join("");

      selectedSize = sizeOptions[0].size;
      selectedPrice = sizeOptions[0].price;

      modalSizeContainer.querySelectorAll(".size-option-btn").forEach(btn => {
        btn.addEventListener("click", () => {
          triggerHapticFeedback();
          modalSizeContainer.querySelectorAll(".size-option-btn").forEach(b => b.classList.remove("active"));
          btn.classList.add("active");
          const index = parseInt(btn.getAttribute("data-index"));
          selectedSize = sizeOptions[index].size;
          selectedPrice = sizeOptions[index].price;
          modalProductPrice.textContent = `${selectedPrice.toLocaleString()} ₸ / ${tUnit}`;
        });
      });
    }
  } else {
    if (modalSizeGroup) {
      modalSizeGroup.classList.add("hidden");
      modalSizeGroup.open = false;
    }
  }

  modalProductPrice.textContent = `${selectedPrice.toLocaleString()} ₸ / ${tUnit}`;

  // Clean listeners
  modalMinusBtn.onclick = () => {
    triggerHapticFeedback();
    let qty = parseInt(modalQtyVal.textContent);
    if (qty > 1) {
      qty--;
      modalQtyVal.textContent = qty;
    }
  };

  modalPlusBtn.onclick = () => {
    triggerHapticFeedback();
    let qty = parseInt(modalQtyVal.textContent);
    if (exceedsProductStock(p, qty + 1) || (normalizeStockValue(p.stock) !== null && qty >= normalizeStockValue(p.stock))) {
      const maxS = normalizeStockValue(p.stock);
      const tMaxStockAlert = window.i18n && window.i18n.getCurrentLanguage() === "kk"
        ? `Кешіріңіз, бұл тауардан қоймада тек ${maxS} дана бар.`
        : `Извините, доступно только ${maxS} шт. этого товара.`;
      showAlert(tMaxStockAlert);
      return;
    }
    qty++;
    modalQtyVal.textContent = qty;
  };

  if (modalAddBtn) {
    modalAddBtn.onclick = () => {
      triggerHapticFeedback();
      const qty = parseInt(modalQtyVal.textContent);
      addToCart(activePreviewProductId, qty, selectedSize, selectedPrice);
      closeProductPreview();
      // Open cart automatically to show it
      openModal(cartSidebar, cartOverlay);
    };
  }

  openProductPreviewModal();
}

/** Open product preview overlay and lock page scroll. */
function openProductPreviewModal() {
  if (!previewModal) return;
  const wasOpen = previewModal.classList.contains("open");
  previewModal.style.display = "";
  previewModal.classList.add("open");
  if (!wasOpen) lockBodyScroll();
  triggerHapticFeedback();
  // Ensure sticky actions / add button are in view after paint
  requestAnimationFrame(() => {
    const actions = previewModal.querySelector(".modal-sticky-actions");
    if (actions) actions.scrollIntoView({ block: "nearest", behavior: "instant" });
  });
}

/** Fully close product preview and release scroll lock. */
function closeProductPreview() {
  if (!previewModal) return;
  const wasOpen = previewModal.classList.contains("open");
  previewModal.classList.remove("open");
  previewModal.style.display = "";
  const container = previewModal.querySelector(".modal-container");
  if (container) {
    container.style.transform = "";
    container.style.transition = "";
  }
  if (wasOpen) unlockBodyScroll();
  // Safety: if no other overlays hold the lock, force-clear stuck body lock
  const anyOpen =
    document.querySelector(".modal-overlay.open") ||
    document.querySelector(".cart-sidebar.open") ||
    document.querySelector(".mobile-drawer.open");
  if (!anyOpen && bodyScrollLockCount > 0) {
    bodyScrollLockCount = 0;
    document.documentElement.classList.remove("modal-open");
    document.body.classList.remove("modal-open");
    document.body.style.top = "";
    window.scrollTo(0, bodyScrollLockY);
  }
  activePreviewProductId = null;
}

// Add Item to Cart
function addToCart(productOrId, qty, selectedSize, selectedPrice) {
  let p;
  if (typeof productOrId === "object") {
    p = productOrId;
  } else {
    p = products.find(prod => prod.id === productOrId);
  }
  if (!p) return;

  if (isProductOutOfStock(p)) return;

  const cartItemId = p.id + (selectedSize ? "_" + selectedSize : "");
  const existing = cart.find(item => item.cartItemId === cartItemId);
  const currentQtyInCart = existing ? existing.qty : 0;
  if (exceedsProductStock(p, currentQtyInCart + qty)) {
    const maxS = normalizeStockValue(p.stock);
    const tMaxStockAlert = window.i18n && window.i18n.getCurrentLanguage() === "kk"
      ? `Кешіріңіз, бұл тауардан қоймада тек ${maxS} дана бар.`
      : `Извините, доступно только ${maxS} шт. этого товара.`;
    showAlert(tMaxStockAlert);
    return;
  }

  if (existing) {
    existing.qty += qty;
  } else {
    cart.push({
      cartItemId: cartItemId,
      product: p,
      qty: qty,
      selectedSize: selectedSize || null,
      price: selectedPrice !== undefined ? selectedPrice : p.price
    });
  }

  updateCartUi();
}

// Remove Item from Cart
function removeFromCart(cartItemId) {
  cart = cart.filter(item => (item.cartItemId || item.product.id) !== cartItemId);
  updateCartUi();
}

function changeCartItemQty(cartItemId, newQty) {
  const item = cart.find(i => (i.cartItemId || i.product.id) === cartItemId);
  if (item) {
    if (exceedsProductStock(item.product, newQty)) {
      const maxS = normalizeStockValue(item.product.stock);
      const tMaxStockAlert = window.i18n && window.i18n.getCurrentLanguage() === "kk"
        ? `Кешіріңіз, бұл тауардан қоймада тек ${maxS} дана бар.`
        : `Извините, доступно только ${maxS} шт. этого товара.`;
      showAlert(tMaxStockAlert);
      return;
    }
    item.qty = newQty;
    if (item.qty <= 0) {
      removeFromCart(cartItemId);
    } else {
      updateCartUi();
      updateLocationUi();
    }
  }
}

// Update Cart Display & Badges
function updateStickyBarUi(totalItems, subtotal) {
  const stickyBar = document.getElementById("sticky-bottom-bar");
  const stickyBadge = document.getElementById("sticky-bar-badge");
  const stickyTotal = document.getElementById("sticky-bar-total");
  if (stickyBar) {
    if (totalItems > 0) {
      if (stickyBar.classList.contains("hidden")) {
        stickyBar.classList.remove("hidden", "hiding");
        // Re-trigger bounce animation
        stickyBar.style.animation = "none";
        stickyBar.offsetHeight; // force reflow
        stickyBar.style.animation = "";
      }
      document.body.classList.add("has-sticky-bar");
      if (stickyBadge) stickyBadge.textContent = totalItems;
      if (stickyTotal) stickyTotal.textContent = subtotal.toLocaleString() + " ₸";
    } else {
      if (!stickyBar.classList.contains("hidden")) {
        stickyBar.classList.add("hiding");
        stickyBar.addEventListener("animationend", () => {
          stickyBar.classList.add("hidden");
          stickyBar.classList.remove("hiding");
          document.body.classList.remove("has-sticky-bar");
        }, { once: true });
      }
    }
  }
}

